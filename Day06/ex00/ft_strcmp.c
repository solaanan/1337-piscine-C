void	ft_cmp(int *tb, char *s1, char *s2)
{
	tb[2] = s1[tb[0]] - s2[tb[1]];
	tb[0] += 1;
	tb[1] += 1;
}

int		ft_strcmp(char *s1, char *s2)
{
	int tb[3];

	tb[0] = 0;
	tb[1] = 0;
	while (s1[tb[0]] != '\0' || s2[tb[1]] != '\0')
	{
		if (s1[tb[0]] == s2[tb[1]])
			ft_cmp(tb, s1, s2);
		if (s1[tb[0]] != s2[tb[1]])
		{
			ft_cmp(tb, s1, s2);
			break ;
		}
	}
	return (tb[2]);
}
